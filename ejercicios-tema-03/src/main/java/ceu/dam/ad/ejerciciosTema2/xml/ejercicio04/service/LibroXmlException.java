package ceu.dam.ad.ejerciciosTema2.xml.ejercicio04.service;

public class LibroXmlException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -3960676056642958421L;

	public LibroXmlException() {
	}

	public LibroXmlException(String message) {
		super(message);
	}

	public LibroXmlException(Throwable cause) {
		super(cause);
	}

	public LibroXmlException(String message, Throwable cause) {
		super(message, cause);
	}

	public LibroXmlException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
